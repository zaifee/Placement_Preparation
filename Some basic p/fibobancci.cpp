
#include <iostream>

using namespace std;
void fib(int n){
    int a = 0;
    int b = 1;
    cout<<a<<" ";
    cout<<b<<" ";
    for(int i=1; i<=n; i++){
       int nextNumber = a+b;
        cout<<nextNumber<<" ";
        a = b;
        b = nextNumber;
    }
}
int main()
{
    fib(10);
    cout<<endl;
    fib(9);
    
    return 0;
}
